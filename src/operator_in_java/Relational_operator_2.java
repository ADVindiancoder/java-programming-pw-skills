package operator_in_java;

public class Relational_operator_2 {

	public static void main(String[] args) {
		int vinay=20;
		int shahu=10;
		System.out.println(vinay>shahu);// true
		System.out.println(shahu<vinay);// true
		System.out.println(shahu>vinay);// false
		System.out.println(vinay<shahu);//false
		System.out.println(vinay<=shahu);// false
		System.out.println(vinay>=shahu);//true
		System.out.println(vinay!=shahu);// true
	}

}
