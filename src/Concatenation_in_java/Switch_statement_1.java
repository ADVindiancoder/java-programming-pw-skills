package Concatenation_in_java;

public class Switch_statement_1 {

	public static void main(String[] args) {
		int number=100;
		
		switch(number) {
		
		case 200:System.out.println("case 1");
		break;
		case 100: System.out.println("case 2");
		break;
		case 300:  System.out.println("case 3");
		break;
		default: System.out.println("case 4");
		}
	}

}
